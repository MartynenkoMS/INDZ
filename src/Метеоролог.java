/**
 * Клас, що представляє метеоролога
 */
public class Метеоролог extends УчасникМетеосистеми implements IПланСповіщень {
    private String спеціалізація;

    /**
     * Конструктор класу Метеоролог
     * @param id ідентифікатор метеоролога
     * @param name ім'я метеоролога
     * @param спец спеціалізація метеоролога
     */
    public Метеоролог(int id, String name, String спец) {
        super(id, name);
        this.спеціалізація = спец;
    }

    /**
     * Публікація прогнозу
     */
    public void публікуватиПрогноз() {
        System.out.println("Метеоролог " + getName() + " публікує прогноз погоди");
    }

    /**
     * Публікація попередження
     */
    public void публікуватиПопередження() {
        System.out.println("Метеоролог " + getName() + " публікує попередження про небезпечні явища");
    }

    /**
     * Фінальний метод аналізу точності прогнозів
     */
    public final void аналізуватиТочність() {
        System.out.println("Метеоролог " + getName() + " аналізує точність прогнозів");
    }

    @Override
    public String сформуватиСповіщення() {
        return "Сповіщення від метеоролога " + getName() + " (" + спеціалізація + ")";
    }
}